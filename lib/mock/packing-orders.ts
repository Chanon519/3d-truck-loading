import type { CargoItem, PackingOrder } from "@/lib/types";

export const DEMO_CONTAINER_NO = "TCLU 482913-7";

function orderWeightKg(items: CargoItem[]): number {
  return items.reduce((sum, it) => sum + it.qty * it.weightKg, 0);
}

function orderVolumeM3(items: CargoItem[]): number {
  const cm3 = items.reduce(
    (sum, it) => sum + it.qty * it.length * it.width * it.height,
    0
  );
  return Math.round((cm3 / 1_000_000) * 100) / 100;
}

function buildOrder(
  base: Omit<PackingOrder, "totalWeightKg" | "totalVolumeM3">
): PackingOrder {
  return {
    ...base,
    totalWeightKg: orderWeightKg(base.items),
    totalVolumeM3: orderVolumeM3(base.items),
  };
}

